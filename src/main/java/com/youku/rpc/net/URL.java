package com.youku.rpc.net;

public class URL {

	private String ip;

	private int port;

	public URL(String ip, int port) {
		super();
		this.ip = ip;
		this.port = port;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}

	@Override
	public String toString() {
		return "url[" + ip + ":" + port + "]";
	}
}
